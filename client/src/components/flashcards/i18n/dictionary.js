export function normalizeLang(code, fallback = "en") {
  const allowed = new Set(["de", "en", "uk"]);
  return allowed.has(code) ? code : fallback;
}

export function getT(lang) {
  const l = normalizeLang(lang, "en");
  return DICT[l] || DICT.en;
}

